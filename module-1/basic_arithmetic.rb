# This Ruby script demonstrates basic arithmetic operations using variables.

# Declare variables
number1 = 10
number2 = 5

# Perform arithmetic operations
addition = number1 + number2
subtraction = number1 - number2
multiplication = number1 * number2
division = number1 / number2

# Output the results
puts "Number 1: #{number1}"
puts "Number 2: #{number2}"
puts "Addition: #{number1} + #{number2} = #{addition}"
puts "Subtraction: #{number1} - #{number2} = #{subtraction}"
puts "Multiplication: #{number1} * #{number2} = #{multiplication}"
puts "Division: #{number1} / #{number2} = #{division}"
